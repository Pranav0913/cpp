//SWITCH CASE
#include<iostream>
using namespace std;
int main()
{
    char p;
    cout<<"Enter the value of p";
    cin>>p;
    switch(p)
    {
    	case 'a':
    		cout<<"character is a";
    		break;
	
	    case 'b':
	    	cout<<"character is b";
	    	break;
	    	
	    	default:
	    		cout<<"Invalid";
    }

	

	return 0;
}
